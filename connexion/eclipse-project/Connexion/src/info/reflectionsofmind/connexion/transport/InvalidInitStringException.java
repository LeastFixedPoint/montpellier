package info.reflectionsofmind.connexion.transport;

public class InvalidInitStringException extends TransportException
{
	public InvalidInitStringException()
	{
		super();
	}

	public InvalidInitStringException(String arg0, Throwable arg1)
	{
		super(arg0, arg1);
	}

	public InvalidInitStringException(String arg0)
	{
		super(arg0);
	}

	public InvalidInitStringException(Throwable arg0)
	{
		super(arg0);
	}
}
