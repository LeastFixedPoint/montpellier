package info.reflectionsofmind.connexion.core.board.geometry;

public class GeometryException extends RuntimeException
{

	private static final long serialVersionUID = 1L;

	public GeometryException()
	{
		super();
	}

	public GeometryException(final String arg0, final Throwable arg1)
	{
		super(arg0, arg1);
	}

	public GeometryException(final String arg0)
	{
		super(arg0);
	}

	public GeometryException(final Throwable arg0)
	{
		super(arg0);
	}

}
