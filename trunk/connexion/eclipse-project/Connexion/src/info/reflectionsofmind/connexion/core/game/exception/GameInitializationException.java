package info.reflectionsofmind.connexion.core.game.exception;

public class GameInitializationException extends Exception
{

	private static final long serialVersionUID = 1L;

	public GameInitializationException()
	{
		super();
	}

	public GameInitializationException(String arg0, Throwable arg1)
	{
		super(arg0, arg1);
	}

	public GameInitializationException(String arg0)
	{
		super(arg0);
	}

	public GameInitializationException(Throwable arg0)
	{
		super(arg0);
	}

}
